//Function to find the data of nth node from the end of a linked list.
int getNthFromLast(Node *head, int n)
{
    Node* head1=head;
    Node* head2=head;
    int count_nodes=0;
    while(head1){
        head1=head1->next;
        count_nodes++;
    }
    if(n>count_nodes){
        return -1; // not possible
    }
    while(count_nodes>n){
       head2=head2->next;
       count_nodes--;
    }
    return head2->data;
    
    
    
}

